function createHtmlTagEntry(tag: string): TagEntry {
  const voidTag = VOID_HTML_TAGS.has(tag);
  const label = `<${tag}>`;
  const example = voidTag
    ? `<${tag} />`
    : `<${tag}>Conteudo de exemplo</${tag}>`;

  return {
    key: `html_${tag}`,
    label,
    category: "html",
    tutorial: `A tag ${label} e um elemento HTML usado na estrutura da pagina.`,
    example,
    aliases: [tag, `tag ${tag}`]
  };
}

const htmlEntries = HTML_TAGS.map(createHtmlTagEntry);

const map = new Map<string, TagEntry>();
for (const tag of [...CORE_TAGS, ...htmlEntries]) {
  map.set(tag.key, tag);
}

export const WORLD_TAGS: TagEntry[] = Array.from(map.values()).sort((a, b) =>
  a.label.localeCompare(b.label)
);

export function normalizeSearch(value: string) {
  return slugify(value).replace(/_/g, " ");
}

